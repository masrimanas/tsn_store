class Product {
  final int id;
  final List<String> gender;
  final String name;
  final int price;
  final String urlImage;
  final List<String> size;

  Product({
    required this.id,
    required this.gender,
    required this.name,
    required this.price,
    required this.urlImage,
    required this.size,
  });
}
